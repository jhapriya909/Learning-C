#include <stdio.h>

// Function to print multiplication table
void multiplicationTable(int n) {
    printf("Multiplication Table of %d:\n", n);
    for(int i = 1; i <= 10; i++) {
        printf("%d x %d = %d\n", n, i, n * i);
    }
}

int main() {
    multiplicationTable(7);
    return 0;
}
